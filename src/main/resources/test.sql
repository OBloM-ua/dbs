CREATE TABLE lv (
  id             INTEGER,
  bezeichnung    VARCHAR(40),
  typ            VARCHAR(40),
  anzStudierende INTEGER,
  PRIMARY KEY (id)
);

CREATE TABLE buch (
  isbn      INTEGER,
  titel     VARCHAR(40),
  autor     VARCHAR(40),
  exemplare INTEGER,
  PRIMARY KEY (isbn)
);

CREATE TABLE empfohlen (
  id   INTEGER,
  isbn INTEGER,
  art  VARCHAR(40),
  PRIMARY KEY (id, isbn),
  FOREIGN KEY (id) REFERENCES lv (id),
  FOREIGN KEY (isbn) REFERENCES buch (isbn)
);

